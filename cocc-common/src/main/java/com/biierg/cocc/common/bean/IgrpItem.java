import java.io.Serializable;
import java.util.Date;

/**
 * Desc: 组团项
 * Created by dw on 2017/2/25.
 */
public class IgrpItem implements Serializable {
    private String id;//组团项ID
    private String name;//组团项名字
    private String url;//调用地址
    private String parameter;//调用参数
    private int status;//使用状态 0：正常使用 1：停用
    private String creator;//创建者
    private Date createTime;//创建时间
    private String updator;//修改者
    private Date updateTime;//修改时间
    private String description;//描述
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getParameter() {
        return parameter;
    }

    public void setParameter(String parameter) {
        this.parameter = parameter;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getCreator() {
        return creator;
    }

    public void setCreator(String creator) {
        this.creator = creator;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public String getUpdator() {
        return updator;
    }

    public void setUpdator(String updator) {
        this.updator = updator;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

}
